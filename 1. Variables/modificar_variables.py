# Variables en Python

# Declaracion e inicializacion de variables
edad = 21
altura = 1.80
pais = 'España'

# Acceder a las variables
print('Valores iniciales: ')
print('Edad:', edad)
print('Altura:', altura)
print('Pais:', pais)

# Modificar el valor de una variable
edad = 22
altura = 1.60

# Acceder a las variables
print('Valores modificados: ')
print('Edad:', edad)
print('Altura:', altura)
print('Pais:', pais)

# En python el tipo es dinamico
edad = 'treinta'
print('Edad:', edad)

# Si queremos acceder a una variable  no declarada manda error
telefono = '55443322'
print('Teléfono:', telefono)
